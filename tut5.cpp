# include<iostream>

using namespace std;

int main(){
    int num1, num2;

    cout<<"Entre the value of num1: \n "; /* '<<' is called Insertion Operator */
    cin>>num1;/* '>>' is called Extraction Operator*/ 

    cout<<"Entre the value of num2: \n "; /* '<<' is called Insertion Operator */
    cin>>num2;/* '>>' is called Extraction Operator*/

    cout<<"The sum is " << num1+num2 ;

    return 0;
}
